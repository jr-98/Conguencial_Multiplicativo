    public void present(Double[] serie) {
        String print = "";
        for (int i = 1; i <= serie.length; i++) {
            if (i == 1) {
                print += "n\t" + "Xn\n" + i + "\t" + serie[i - 1] + "\n";
                textAreaSalidaPoker.setText(print);
            } else {
                print += i + "\t" + serie[i - 1] + "\n";
                textAreaSalidaPoker.setText(print);
            }
        }

    }

